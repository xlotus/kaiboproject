package cn.cibn.kaibo.model;

import cn.cibn.kaibo.network.Constants;

public class ModelWrapper<T> {
    private T data;
    private int code;
    private String errMsg;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public boolean isSuccess() {
        return code == Constants.CODE_SUCCESS;
    }

    public String getErrMsg() {
        return errMsg;
    }

    public void setErrMsg(String errMsg) {
        this.errMsg = errMsg;
    }
}
